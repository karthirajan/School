package org.onlineclass;

public class Onlineclass {
	
		
		public void class9() {
			System.out.println("class 9th");
			
		}
		public void class10() {
			System.out.println("class 10th");
			
		}
		public void onlineTime() {
			String f = "class starts at 8am";
			System.out.println(f);
			
		}
		public static void main(String[] args) {
			Onlineclass o = new Onlineclass();
			o.class10();
			o.class9();
			o.onlineTime();
		}
	

}
